class VideoModel {
  final String thumbnail;
  final String title;
  final String channelName;
  final String views;
  final String postedTime;
  final String channelAvatar;

  VideoModel({
    required this.thumbnail,
    required this.title,
    required this.channelName,
    required this.views,
    required this.postedTime,
    required this.channelAvatar,
  });
}